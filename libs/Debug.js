scope.DescribeElement = function(oElement){
  return "[id=" + oElement.id +
         ",value=" + oElement.value +
         ",text=" + oElement.text +
         ",class=" + oElement.className +
         ",tabIndex=" + oElement.tabIndex + "]";
}

scope.Alert = function(sAlert){
  if (scope.bDebug){
    window.alert(sAlert);
  }
}


